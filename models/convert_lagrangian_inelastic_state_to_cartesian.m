function result = convert_lagrangian_inelastic_state_to_cartesian(x,p)
    load('models/model_lagrangian_inelastic_info')
    for i = 1:size(x,2)
        [result.position0(:,:,i), ...
        result.velocity0(:,:,i), ...
        result.direction0(:,:,i), ...
        result.position(:,:,i), ...
        result.velocity(:,:,i), ...
        result.direction(:,:,i)] ...
            = generalized_coordinates_generated(...
                x(ix_theta0,      i), ...
                x(ix_theta0_dot,  i), ...
                x(ix_phi0,        i), ...
                x(ix_phi0_dot,    i), ...
                x(ix_length0,     i), ...
                x(ix_length0_dot, i), ...
                0, ...
                0, ...
                x(ix_theta,       i).', ...
                x(ix_theta_dot,   i).', ...
                x(ix_phi,         i).', ...
                x(ix_phi_dot,     i).', ...
                p(ip_length        ).', ...
                zeros(1,n_aircraft)   , ...
                zeros(1,n_aircraft)   , ...
                zeros(1,n_aircraft)    ...
            );
    end
end

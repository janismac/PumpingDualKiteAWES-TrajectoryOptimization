function generate_struct_wrapper_function(filename, output_names, base_function_name, wrapper_function_name)

    % The code generated by matlabFunction() returns an nameless list
    % of outputs. Generate a wrapper that outputs named struct fields.
    output_name_list = join(output_names,',');
    fid = fopen(filename, 'w');
    fprintf(fid, 'function outputs = %s(varargin)\n', wrapper_function_name);
    fprintf(fid, '    \n');
    fprintf(fid, '    [%s] ...\n', output_name_list{1});
    fprintf(fid, '        = %s(varargin{:});\n', base_function_name);
    fprintf(fid, '    \n');
    fprintf(fid, '    outputs = struct;\n');
    for i = 1:length(output_names)
        fprintf(fid, '    outputs.%s = %s;\n', output_names{i}, output_names{i});
    end
    fprintf(fid, '    \n');
    fprintf(fid, 'end\n');
    fprintf(fid, '\n');
    fclose(fid);
    
end

